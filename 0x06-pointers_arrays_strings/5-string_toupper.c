#include "main.h"
#include <string.h>

/**
* string_toupper - reverse the contents of an array of intergers
* @str: string
* Return: string
*/

char *string_toupper(char *str)
{
	int i;

	for (i = 0; str[i] != '\0'; i++)
	{
		if (str[i] >= 97 && str[i] <= 122)
		{
			str[i] = str[i] - 32;
		}
	}
	return (str);
}
